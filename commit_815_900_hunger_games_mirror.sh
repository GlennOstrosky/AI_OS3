## The Ten Parallels

### 1. The Capitol → Davos / WEF / G7
Centralized wealth, insulated from consequence, designing policy for districts they never visit. They eat while the districts starve. They legislate while the districts comply.

> *"Shall the throne of iniquity have fellowship with thee, which frameth mischief by a law?"* — Psalm 94:20

### 2. The Districts → The Fading Towns
Edmonton, Winnipeg, Outlook, the hollowed cities. Resource extraction zones producing wealth consumed elsewhere. District 12 mined coal. Alberta mines oil. The Capitol takes both.

> *"Woe unto them that decree unrighteous decrees."* — Isaiah 10:1

