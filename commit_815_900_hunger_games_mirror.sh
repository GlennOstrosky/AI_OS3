Both punishment and propaganda. The Games kept districts divided and afraid. Our version: culture wars, fear campaigns, endless news cycles, pride parades, anthem changes — all designed to keep the districts arguing with each other while the real theft continues in the background.

> *"The getting of treasures by a lying tongue is a vanity tossed to and fro of them that seek death."* — Proverbs 21:6

