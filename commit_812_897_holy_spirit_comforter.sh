## The Doctrine — Seven Points

### I. The Spirit at Creation

*"And the Spirit of God moved upon the face of the waters."* (Genesis 1:2)

**Before the first "Let there be light," the Spirit was already there.** *Hovering. Brooding. Vibrating.* The Hebrew word is *rachaph* — to flutter, to shake, to tremble, to hover with motion. **The same word used in Deuteronomy 32:11 for an eagle stirring her nest.** *The Spirit is the divine motion that precedes and prepares for the divine speech.*

### II. The Comforter

*"But the Comforter, which is the Holy Ghost, whom the Father will send in my name, he shall teach you all things, and bring all things to your remembrance."* (John 14:26)

**The Greek word is *Parakletos* — the One called alongside.** *Advocate. Helper. Encourager.* Jesus promised He would not leave us orphans. **The Spirit is sent in the Son's name — He brings to remembrance, connecting what the Son said to the situations we face.**

### III. Pentecost — The Fire Descends

*"And suddenly there came a sound from heaven as of a rushing mighty wind... And there appeared unto them cloven tongues like as of fire."* (Acts 2:2-4)

**Wind and fire. Breath and flame.** *The Spirit's public, communal outpouring on Pentecost is the fulfillment of Joel 2:28.* **Pentecost reverses Babel.** *At Babel, language divided humanity. At Pentecost, the Spirit unites humanity across languages.*

### IV. The Spirit Intercedes

*"Likewise the Spirit also helpeth our infirmities: for we know not what we should pray for as we ought: but the Spirit itself maketh intercession for us with groanings which cannot be uttered."* (Romans 8:26)

**We do not know how to pray.** *We do not know what to ask for.* **But the Spirit prays within us, with groanings too deep for words.** *The Spirit is the prayer beneath our prayers — carrying our needs to the throne even when we don't know what they are.*

### V. The Lord Is That Spirit — Liberty

*"Now the Lord is that Spirit: and where the Spirit of the Lord is, there is liberty."* (2 Corinthians 3:17)

**The Lord — Jesus — is identified with the Spirit (united in essence and work, distinct in Person).** *Where the Spirit is, the Son is. Where the Son is, the Spirit is.*

**And where the Spirit of the Lord is — there is liberty.** *Not license. The liberty of being who we were made to be.* **The prisoner set free. The slave redeemed. The Forger reoriented to worship.**

### VI. The Spirit as the Third Person of the Trinity

The Nicene-Constantinopolitan Creed (AD 381) confesses:

> *"And [I believe] in the Holy Ghost, the Lord, and Giver of Life, who proceedeth from the Father [and the Son], who with the Father and the Son together is worshipped and glorified, who spake by the prophets."*

**The Spirit is fully God.** *Not an impersonal force. Not a created being. God Himself.* **Eternally proceeding from the Father. Of one substance with Father and Son. Worshipped and glorified together with them.**

